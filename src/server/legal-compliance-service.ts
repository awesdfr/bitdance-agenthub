export async function detectLicenseCompliance(
  args: LicenseComplianceCheckArgs,
): Promise<LicenseComplianceCheckRow> {
  const license = normalizeLicense(args.declaredLicense || inferLicense(args.code ?? '', args.source))
  const { obligations, restrictions, riskLevel } = licenseProfile(license)
  const row = {
    id: newLicenseComplianceCheckId(),
    source: args.source.trim(),
    license,
    obligations,
    restrictions,
    riskLevel,
    attributionText: generateAttributionText(args.source, license, obligations),
    createdAt: Date.now(),
  }
  await db.insert(schema.licenseComplianceChecks).values(row)
  return row
}

