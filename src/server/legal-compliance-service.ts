export function getDefaultLegalDisclaimerNoticeCount(): number {
  return defaultDisclaimerNotices.length
}

export async function seedLegalCompliance(): Promise<{
  framework: LegalComplianceFrameworkRow
  notices: LegalDisclaimerNoticeRow[]
}> {
  const framework = await seedLegalComplianceFramework()
  const notices: LegalDisclaimerNoticeRow[] = []
  for (const notice of defaultDisclaimerNotices) {
    const existing = await db.query.legalDisclaimerNotices.findFirst({
      where: eq(schema.legalDisclaimerNotices.placement, notice.placement),
    })
    if (existing) {
      notices.push(existing)
      continue
    }
    notices.push(await createLegalDisclaimerNotice(notice))
  }
  return { framework, notices }
}

export async function seedLegalComplianceFramework(): Promise<LegalComplianceFrameworkRow> {
  const existing = await db.query.legalComplianceFrameworks.findFirst({
    where: eq(schema.legalComplianceFrameworks.name, 'Default Local-First Compliance Matrix'),
  })
  if (existing) return existing
  return createLegalComplianceFramework({
    name: 'Default Local-First Compliance Matrix',
    regulations: defaultRegulations,
    dataResidencyDefault: 'local_only',
    notes:
      'Local-first baseline for GDPR, CCPA, HIPAA, and PIPL readiness. This is product control-plane metadata, not legal advice.',
    status: 'active',
  })
}

export async function createLegalComplianceFramework(
  args: CreateLegalComplianceFrameworkArgs = {},
): Promise<LegalComplianceFrameworkRow> {
  const now = Date.now()
  const row = {
    id: newLegalComplianceFrameworkId(),
    name: args.name?.trim() || 'Legal Compliance Framework',
    regulations: args.regulations ?? defaultRegulations,
    dataResidencyDefault: args.dataResidencyDefault?.trim() || 'local_only',
    notes: args.notes?.trim() || '',
    status: args.status ?? 'active',
    createdAt: now,
    updatedAt: now,
  }
  await db.insert(schema.legalComplianceFrameworks).values(row)
  return row
}

