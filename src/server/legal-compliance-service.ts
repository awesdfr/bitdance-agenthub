function inferLicense(code: string, source: string): string {
  const text = `${source}\n${code}`.toLowerCase()
  if (text.includes('apache-2.0') || text.includes('apache license')) return 'Apache-2.0'
  if (text.includes('gpl-3.0') || text.includes('gnu general public license')) return 'GPL-3.0'
  if (text.includes('bsd')) return 'BSD'
  if (text.includes('mit license') || text.includes('permission is hereby granted')) return 'MIT'
  return 'unknown'
}

function normalizeLicense(license: string): string {
  const value = license.trim()
  if (!value) return 'unknown'
  const lower = value.toLowerCase()
  if (lower === 'mit') return 'MIT'
  if (lower === 'apache' || lower === 'apache-2.0') return 'Apache-2.0'
  if (lower === 'gpl' || lower === 'gpl-3.0') return 'GPL-3.0'
  if (lower === 'bsd') return 'BSD'
  return value
}

function licenseProfile(license: string): {
  obligations: string[]
  restrictions: string[]
  riskLevel: LicenseRiskLevel
} {
  if (license === 'MIT') {
    return {
      obligations: ['preserve_copyright_notice', 'include_license_text'],
      restrictions: [],
      riskLevel: 'low',
    }
  }
  if (license === 'Apache-2.0') {
    return {
      obligations: ['preserve_copyright_notice', 'include_license_text', 'state_significant_changes'],
      restrictions: ['respect_patent_license_terms'],
      riskLevel: 'low',
    }
  }
  if (license === 'BSD') {
    return {
      obligations: ['preserve_copyright_notice', 'include_license_text'],
      restrictions: ['do_not_use_author_names_for_endorsement'],
      riskLevel: 'medium',
    }
  }
  if (license === 'GPL-3.0') {
    return {
      obligations: ['preserve_copyright_notice', 'include_license_text', 'provide_corresponding_source'],
      restrictions: ['copyleft_obligations_for_distribution', 'review_before_closed_source_use'],
      riskLevel: 'high',
    }
  }
  return {
    obligations: ['verify_license_before_reuse'],
    restrictions: ['do_not_reuse_without_review'],
    riskLevel: 'critical',
  }
}

function generateAttributionText(source: string, license: string, obligations: string[]): string {
  return `Source: ${source}; license: ${license}; obligations: ${obligations.join(', ')}.`
}
